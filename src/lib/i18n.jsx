const ACTIVITY_KEYS_BY_LABEL = Object.entries(DEFAULT_ACTIVITY_LABELS).reduce((acc, [id, label]) => {
  acc[normalizeKnownLabel(label)] = ACTIVITY_KEYS_BY_ID[id]
  return acc
}, {
  [normalizeKnownLabel('Eat')]: 'activity.eat',
})

function normalizeKnownLabel(value) {
  return String(value || '')
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '')
}

function normalizePetName(name) {
  return String(name || '').trim().toLowerCase()
}

const I18nContext = createContext(null)

export function I18nProvider({ children }) {
  const initialSystem = useMemo(() => resolveSystemLocale(), [])
  const [systemLanguage, setSystemLanguage] = useState(initialSystem.language)
  const [systemRegion, setSystemRegion] = useState(initialSystem.region)
  const [languageSetting, setLanguageSettingState] = useState(() =>
    readStoredSetting(LANGUAGE_STORAGE_KEY, isLanguageSetting, 'system'),
  )
  const [regionSetting, setRegionSettingState] = useState(() =>
    readStoredSetting(REGION_STORAGE_KEY, isRegionSetting, 'system'),
  )

  useEffect(() => {
    if (typeof document === 'undefined') return undefined

    const refresh = () => {
      const next = resolveSystemLocale()
      setSystemLanguage(next.language)
      setSystemRegion(next.region)
    }

    const onVisibilityChange = () => {
      if (document.visibilityState === 'visible') refresh()
    }

    window.addEventListener('languagechange', refresh)
    document.addEventListener('visibilitychange', onVisibilityChange)
    return () => {
      window.removeEventListener('languagechange', refresh)
      document.removeEventListener('visibilitychange', onVisibilityChange)
    }
  }, [])

  const resolvedLanguage = languageSetting === 'system' ? systemLanguage : languageSetting
  const resolvedRegion = regionSetting === 'system' ? systemRegion : regionSetting
  const intlLocale = makeIntlLocale(resolvedLanguage, resolvedRegion)

  useEffect(() => {
    if (typeof document === 'undefined') return
    document.documentElement.lang = intlLocale
    document.documentElement.dir = isRtlLanguage(resolvedLanguage) ? 'rtl' : 'ltr'
    document.documentElement.dataset.locale = resolvedLanguage
    document.documentElement.dataset.region = resolvedRegion
  }, [intlLocale, resolvedLanguage, resolvedRegion])

  const t = useCallback(
    (key, values) => {
      const map = TRANSLATIONS[resolvedLanguage] || EN
      const raw = map[key] ?? EN[key] ?? key
      return interpolate(raw, values)
    },
    [resolvedLanguage],
  )

  const languageName = useCallback(
    (code, form = 'native') => {
      if (code === 'system') return t('locale.browserDefault')
      return LANGUAGE_NAMES[code]?.[form] || code
    },
    [t],
  )

  const regionName = useCallback(
    (code, form = 'localized') => {
      if (code === 'system') return t('locale.browserDefault')
      if (form === 'english') return REGION_NAMES[code] || code
      try {
        return new Intl.DisplayNames([intlLocale], { type: 'region' }).of(code) || REGION_NAMES[code] || code
      } catch {
        return REGION_NAMES[code] || code
      }
    },
    [intlLocale, t],
  )

  const setLanguageSetting = useCallback((next) => {
    if (!isLanguageSetting(next)) return
    setLanguageSettingState(next)
    writeStoredSetting(LANGUAGE_STORAGE_KEY, next)
  }, [])

  const setRegionSetting = useCallback((next) => {
    if (!isRegionSetting(next)) return
    setRegionSettingState(next)
    writeStoredSetting(REGION_STORAGE_KEY, next)
  }, [])

  const formatDate = useCallback(
    (date, options) => {
      try {
        return new Intl.DateTimeFormat(intlLocale, options).format(new Date(date))
      } catch {
        return new Date(date).toLocaleDateString(undefined, options)
      }
    },
    [intlLocale],
  )

  const formatNumber = useCallback(
    (value, options) => {
      try {
        return new Intl.NumberFormat(intlLocale, options).format(value)
      } catch {
        return String(value)
      }
    },
    [intlLocale],
  )

  const formatDateRange = useCallback(
    (start, end, options = { month: 'short', day: 'numeric' }) => {
      const startDate = new Date(start)
      const endDate = new Date(end)
      try {
        const formatter = new Intl.DateTimeFormat(intlLocale, options)
        if (typeof formatter.formatRange === 'function') {
          return formatter.formatRange(startDate, endDate)
        }
        return `${formatter.format(startDate)} - ${formatter.format(endDate)}`
      } catch {
        return `${startDate.toLocaleDateString()} - ${endDate.toLocaleDateString()}`
      }
    },
    [intlLocale],
  )

  const activityLabel = useCallback(
    (activityOrLabel, maybeId) => {
      const activity = typeof activityOrLabel === 'object' && activityOrLabel !== null
        ? activityOrLabel
        : null
      const raw = activity ? activity.label : activityOrLabel
      const trimmed = String(raw || '').trim()
      const id = activity?.id || maybeId
      const defaultLabel = DEFAULT_ACTIVITY_LABELS[id]
      const keyById = ACTIVITY_KEYS_BY_ID[id]
      const keyByLabel = ACTIVITY_KEYS_BY_LABEL[normalizeKnownLabel(trimmed)]

      if (keyById) {
        if (!trimmed || normalizeKnownLabel(trimmed) === normalizeKnownLabel(defaultLabel)) return t(keyById)
        if (keyByLabel) return t(keyByLabel)
        return trimmed
      }

      if (keyByLabel) return t(keyByLabel)
      return trimmed || t('activity.generic')
    },
    [t],
  )

  const themeLabel = useCallback(
    (themeKey, fallback) => {
      if (!themeKey) return fallback || ''
      const key = `theme.${themeKey}`
      const translated = t(key)
      return translated === key ? fallback || themeKey : translated
    },
    [t],
  )

  const eggName = useCallback(
    (themeKey, fallback) => {
      if (!themeKey) return fallback || t('egg.animals')
      const key = `egg.${themeKey}`
      const translated = t(key)
      return translated === key ? fallback || t('egg.animals') : translated
    },
    [t],
  )

  const petSpeciesName = useCallback(
    (name) => {
      const raw = String(name || '').trim()
      if (!raw) return ''
      return PET_NAME_TRANSLATIONS[resolvedLanguage]?.[normalizePetName(raw)] || raw
    },
    [resolvedLanguage],
  )

  const petChainLabel = useCallback(
    (chainKey, fallback) => {
      const raw = String(fallback || '').trim()
      if (!chainKey) return raw || t('pet.generic')
      return PET_CHAIN_LABEL_TRANSLATIONS[resolvedLanguage]?.[chainKey] || raw || t('pet.generic')
    },
    [resolvedLanguage, t],
  )

  const value = useMemo(() => {
    const resolvedLanguageName = languageName(resolvedLanguage)
    const resolvedRegionName = regionName(resolvedRegion)
    return {
      languageSetting,
      regionSetting,
      resolvedLanguage,
      resolvedRegion,
      systemLanguage,
      systemRegion,
      intlLocale,
      t,
      languageName,
      regionName,
      setLanguageSetting,
      setRegionSetting,
      formatDate,
      formatDateRange,
      formatNumber,
      activityLabel,
      themeLabel,
      eggName,
      petSpeciesName,
      petChainLabel,
      languageSummary:
        languageSetting === 'system'
          ? `${t('locale.browserDefaultShort')} (${languageName(systemLanguage)})`
          : resolvedLanguageName,
      regionSummary:
        regionSetting === 'system'
          ? `${t('locale.browserDefaultShort')} (${regionName(systemRegion)})`
          : resolvedRegionName,
      localeSummary: t('locale.summary', {
        language: resolvedLanguageName,
        region: resolvedRegionName,
      }),
    }
  }, [
    activityLabel,
    eggName,
    formatDate,
    formatDateRange,
    formatNumber,
    intlLocale,
    languageName,
    languageSetting,
    petChainLabel,
    petSpeciesName,
    regionName,
    regionSetting,
    resolvedLanguage,
    resolvedRegion,
    setLanguageSetting,
    setRegionSetting,
    systemLanguage,
    systemRegion,
    t,
    themeLabel,
  ])

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const value = useContext(I18nContext)
  if (!value) throw new Error('useI18n must be used inside I18nProvider')
  return value
}

export function usePageMetadata(title, description) {
  useEffect(() => {
    if (typeof document === 'undefined') return
    if (title) document.title = title
    if (!description) return
    let meta = document.querySelector('meta[name="description"]')
    if (!meta) {
      meta = document.createElement('meta')
      meta.setAttribute('name', 'description')
      document.head.appendChild(meta)
    }
    meta.setAttribute('content', description)
  }, [title, description])
}

export function makeIntlLocale(language, region) {
  const safeLanguage = normalizeLanguageTag(language) || DEFAULT_LANGUAGE
  const safeRegion = normalizeRegionTag(region) || DEFAULT_REGION_BY_LANGUAGE[safeLanguage] || DEFAULT_REGION
  return safeLanguage === 'zh-Hant' ? `zh-Hant-${safeRegion}` : `${safeLanguage}-${safeRegion}`
}

export function resolveLocaleFromTags(tags) {
  const list = Array.isArray(tags) ? tags : [tags]
  for (const tag of list) {
    const language = normalizeLanguageTag(tag)
    if (language) {
      const region = normalizeRegionTag(tag) || DEFAULT_REGION_BY_LANGUAGE[language] || DEFAULT_REGION
      return { language, region }
    }
  }
  return { language: DEFAULT_LANGUAGE, region: DEFAULT_REGION }
}

export function normalizeLanguageTag(tag) {
  if (!tag || typeof tag !== 'string') return null
  const normalized = tag.replace('_', '-').toLowerCase()
  if (normalized.startsWith('en')) return 'en'
  if (
    normalized === 'zh-hant' ||
    normalized.startsWith('zh-hant-') ||
    normalized === 'zh-tw' ||
    normalized === 'zh-hk' ||
    normalized === 'zh-mo'
  ) {
    return 'zh-Hant'
  }
  if (normalized.startsWith('fr')) return 'fr'
  if (normalized.startsWith('es')) return 'es'
  if (normalized.startsWith('de')) return 'de'
  return null
}

export function normalizeRegionTag(tag) {
  if (!tag || typeof tag !== 'string') return null
  const parts = tag.replace('_', '-').split('-')
  // In a multi-subtag locale ("fr-CA") the first subtag is the language and
  // must not be read as a region — the region codes FR/ES/DE collide with the
  // language codes fr/es/de, so scanning the language part would resolve
  // "fr-CA" to FR instead of CA. A bare region code ("CA") is a single part.
  const candidates = parts.length > 1 ? parts.slice(1) : parts
  for (const part of candidates) {
    const upper = part.toUpperCase()
    if (REGIONS.includes(upper)) return upper
  }
  return null
}

function resolveSystemLocale() {
  if (typeof navigator !== 'undefined') {
    const tags = navigator.languages?.length ? navigator.languages : [navigator.language]
    return resolveLocaleFromTags(tags)
  }
  try {
    return resolveLocaleFromTags(Intl.DateTimeFormat().resolvedOptions().locale)
  } catch {
    return { language: DEFAULT_LANGUAGE, region: DEFAULT_REGION }
  }
}

function interpolate(text, values) {
  if (!values) return text
  return text.replace(/\{([a-zA-Z0-9_]+)\}/g, (match, key) => {
    const value = values[key]
    return value == null ? match : String(value)
  })
}

function readStoredSetting(key, guard, fallback) {
  try {
    const value = window.localStorage.getItem(key)
    return guard(value) ? value : fallback
  } catch {
    return fallback
  }
}

function writeStoredSetting(key, value) {
  try {
    if (value === 'system') window.localStorage.removeItem(key)
    else window.localStorage.setItem(key, value)
  } catch {
    // Ignore private-mode and storage-denied failures. The in-memory state
    // still updates for this session.
  }
}

function isLanguageSetting(value) {
  return value === 'system' || LOCALES.includes(value)
}

function isRegionSetting(value) {
  return value === 'system' || REGIONS.includes(value)
}

function isRtlLanguage(language) {
  return ['ar', 'fa', 'he', 'ur'].includes(language)
}
